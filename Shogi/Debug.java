/*
I REALLY Like Unity's Debug.log,
so I decided to use it over System.out.println
*/

public class Debug {
    public static void log(String statement){
        System.out.println(statement);
    }
    public static void log(int statement){
        System.out.println(statement);
    } 
    public static void log(Float statement){
        System.out.println(statement);
    }
    public static void log(Long statement){
        System.out.println(statement);
    }
    public static void log(Double statement){
        System.out.println(statement);
    }
}
